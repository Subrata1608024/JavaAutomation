package class2;

public class primitive {
    public static void main(String[] args) {


        {//primitive integer
            int age =25;
            System.out.println(age);
        }
        {//boolean

            boolean flag = true;
            System.out.println(flag);

        }
        {//long
            long range= -4567854L;
            System.out.println(range);

        }
        {//float
            float range= 124.55f;
            System.out.println(range);

        }
        {//double
            double range= -42.6;
            System.out.println(range);

        }

        {//byte
            byte range= 124;
            System.out.println(range);

        }
        {//short
            short range= -200;
            System.out.println(range);

        }
        {//character
            char range= '\u0051';
            System.out.println(range);

        }

    }

}
